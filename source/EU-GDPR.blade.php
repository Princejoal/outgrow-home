				What is GDPR?
			</h4>
			<p>
			General Data Protection Regulation, commonly referred to as GDPR, is a new piece of European Union legislation intended to standardize data regulation across Europe while providing greater protection and control over data to the consumer. This legal framework replaces the current EU Data Protection Directive (95/46/EC) with additional requirements that you need to be aware of. The new EU data protection regime extends the scope of the EU data protection law to all companies even outside the EU when they process data of EU residents.
			</p>
			<h4 class="Privacy-subheading">Who does GDPR applies to?</h4>
			<p>
				GDPR applies to persons and entities of all sizes that process personal data of EU residents, regardless of where they are based. These regulations apply to both data controllers and data processors, including third parties such as cloud providers. It applies to all 28 EU member states and to entities and organisations outside the EU when processing the data of citizens within it.
			</p>
			<h4 class="Privacy-subheading">What is personal data according to GDPR?</h4>
			<p>
				Anything that contributes or links to identifying an individual will be included, whether it relates to his or her private, professional or public life. It can be anything from a name, a home address, a photo, an email address, bank details, posts on social networking websites, medical information, a computer’s IP address and includes both biometric and genetic data. 
			</p>
			
			<h4 class="Privacy-subheading">GDPR Requirements</h4>
			<ul>
				<li class="li-style">
					<b>Consent</b> - Unbundled consent as well as purpose for using the data, should be stored in an auditable and secured way. (Part of article 7 and 8 of the regulation). Consent related guidelines include rights of access, to be forgotten, and to data portability. Data Controllers and Data Processors have an obligation to clearly communicate these rights to individuals they collect data from. Companies will no longer be able to use long illegible T&Cs full of legalese, as the request for consent must be given in an intelligible and easily accessible form, with the purpose for data processing attached to that consent. Consent now must be clear and distinguishable from other matters and provided in an intelligible and easily accessible form, using clear and plain language. It must be as easy to withdraw consent as it is to give it.
				</li>
				<li class="li-style">
					<b>Data access and rectification</b> - Data subject should have access to collected data. Data subject should be aware if their data is being transferred to other countries. They should be able to download and ask for modification of the collected data.  (Part of article 15 and 16)
				</li>
				<li class="li-style">
					<b>Erasure - Right to be forgotten</b> - Gives a data subject right to ask the organization (controller) to forget or erase personal data. (Part of article 17) 
				</li>
				<li class="li-style">
					<b>Data storage</b> - Store EU data within EU separate from other countries 
				</li>
				<li class="li-style">
					<b>Data protection and security</b> - Required by design, this includes secure storage of data including encryption, data minimization and controlled access to data and applications. (Part of article 25 and 32)
				</li>
			</ul>
